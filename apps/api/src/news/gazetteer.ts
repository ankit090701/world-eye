// Precompute longest-first (so specific names win over shorter substrings) with a
// compiled word-boundary regex per place — built once, reused across all headlines.
const COMPILED = [...PLACES]
  .sort((a, b) => b[0].length - a[0].length)
  .map(([name, lat, lon]) => ({
    name,
    lat,
    lon,
    re: new RegExp(`(^|[^A-Za-z])${name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}([^A-Za-z]|$)`, 'i'),
  }))

export function geoparse(text: string): { place: string; lat: number; lon: number } | null {
  if (!text) return null
  for (const p of COMPILED) {
    if (p.re.test(text)) return { place: p.name, lat: p.lat, lon: p.lon }
  }
  return null
}
